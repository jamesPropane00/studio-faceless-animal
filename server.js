  try {
    rawBody = await readBody(req, COMMUNITY_MAX_FILE_B + 16384)
  } catch {
    return sendJSON(res, 413, { error: `File too large. Max ${COMMUNITY_MAX_FILE_MB}MB.` })
  }

  let body
  try { body = JSON.parse(rawBody.toString('utf8')) } catch {
    return sendJSON(res, 400, { error: 'Invalid request body.' })
  }

  const { username, ph, file_b64, file_type, file_name, title } = body
  if (!username || !ph || !file_b64 || !file_type || !file_name || !title) {
    return sendJSON(res, 400, { error: 'Missing required fields (username, ph, file_b64, file_type, file_name, title).' })
  }

  const u = username.toLowerCase().replace(/[^a-z0-9_-]/g, '')
  if (!u) return sendJSON(res, 400, { error: 'Invalid username.' })

  if (!RADIO_ALLOWED.has(file_type)) {
    return sendJSON(res, 400, { error: 'Only audio files supported (MP3, WAV, OGG, FLAC, M4A, AAC).' })
  }

  let creds
  try { creds = getServiceCreds() } catch {
    return sendJSON(res, 503, { error: 'Server not available.' })
  }

  const ok = await verifyIdentity(creds.host, creds.key, u, ph)
