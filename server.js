async function executeAgentTool(tc, root) {
  const { name, arguments: raw } = tc.function;
  let args;
  try { args = JSON.parse(raw); } catch { return { error: 'Invalid tool arguments JSON' }; }
  try {
    switch (name) {
      case 'read_file': {
        const fp = sanitizePath(args.path, root);
        if (!fs.existsSync(fp)) return { error: 'File not found: ' + args.path };
        const content = fs.readFileSync(fp, 'utf-8');
        const preview = content.length > 5000 ? content.slice(0, 5000) + '\n... (truncated, ' + content.length + ' bytes total)' : content;
        return { content: preview, path: args.path, size: content.length };
      }
      case 'write_file': {
        const fp = sanitizePath(args.path, root);
        fs.mkdirSync(path.dirname(fp), { recursive: true });
        fs.writeFileSync(fp, args.content, 'utf-8');
        return { success: true, path: args.path, bytes: Buffer.byteLength(args.content, 'utf-8') };
      }
      case 'list_dir': {
        const dp = args.path ? sanitizePath(args.path, root) : root;
        const entries = fs.readdirSync(dp, { withFileTypes: true });
        return { files: entries.map(e => ({ name: e.name, type: e.isDirectory() ? 'dir' : 'file' })), path: args.path || '/' };
      }
      case 'execute_command': {
        const { exec } = require('child_process');
        return new Promise(resolve => {
          exec(args.command, { cwd: root, timeout: 30000, maxBuffer: 1024 * 1024 }, (err, stdout, stderr) => {
            const out = (stdout || '').slice(0, 10000);
            const errOut = (stderr || '').slice(0, 5000);
            resolve({ stdout: out, stderr: errOut, exitCode: err ? (err.code || err.killed ? 137 : 1) : 0 });
          });
        });
      }
      case 'web_search': {
        const searxngUrl = process.env.SEARXNG_URL || '';
        const searchApiKey = process.env.SEARCH_API_KEY || global.__SEARCH_API_KEY || '';
        const searchEngine = process.env.SEARCH_ENGINE || 'duckduckgo';
        const q = encodeURIComponent(args.query);
        let results = '';
        if (searxngUrl) {
          try {
            const sRes = await fetch(searxngUrl.replace(/\/+$/, '') + '/search?q=' + q + '&format=json&pageno=1&language=en-US', { signal: AbortSignal.timeout(10000) });
            if (sRes.ok) { const d = await sRes.json(); results = (d.results || []).slice(0, 6).map(r => r.title + ': ' + r.url + '\n' + (r.content || '').slice(0, 300)).join('\n\n'); }
          } catch {}
        }
        if (!results) {
          try {
            const dRes = await fetch('https://html.duckduckgo.com/html/?q=' + q, { headers: { 'User-Agent': 'Mozilla/5.0' }, signal: AbortSignal.timeout(10000) });
            const html = await dRes.text();
            const links = html.match(/<a[^>]+class="result__a"[^>]*>([\s\S]*?)<\/a>/g) || [];
            results = links.slice(0, 5).map((a, i) => {
              const title = a.replace(/<[^>]*>/g, '').trim();
              const urlMatch = a.match(/href="([^"]+)"/);
              return (i + 1) + '. ' + title + (urlMatch ? ' (' + urlMatch[1] + ')' : '');
            }).join('\n');
          } catch {}
        }
        return { results: results || 'No results found.', query: args.query };
      }
      case 'search_code': {
        const results = [];
        function walk(dir) {
          const entries = fs.readdirSync(dir, { withFileTypes: true });
          for (const entry of entries) {
            if (entry.name.startsWith('.') || entry.name === 'node_modules') continue;
            const full = path.join(dir, entry.name);
            if (entry.isDirectory()) walk(full);
            else if (entry.isFile()) {
              try {
                const content = fs.readFileSync(full, 'utf-8');
                const lines = content.split('\n');
                for (let i = 0; i < lines.length; i++) {
                  if (lines[i].match(new RegExp(args.pattern, 'i'))) {
                    const rel = path.relative(root, full);
                    results.push({ file: rel, line: i + 1, content: lines[i].trim() });
                  }
                }
              } catch {}
            }
          }
        }
        walk(root);
        return { matches: results.slice(0, 50), total: results.length };
      }
      case 'find_files': {
        const results = [];
        function walk(dir, depth) {
          if (depth > 8) return;
          try {
            const entries = fs.readdirSync(dir, { withFileTypes: true });
            for (const e of entries) {
              if (e.name.startsWith('.') || e.name === 'node_modules') continue;
              const full = path.join(dir, e.name);
              const rel = path.relative(root, full);
              if (args.pattern) {
                const regex = new RegExp('^' + args.pattern.replace(/\*/g, '.*').replace(/\?/g, '.') + '$');
                if (regex.test(rel)) results.push(rel);
              }
              if (e.isDirectory()) walk(full, depth + 1);
            }
          } catch {}
        }
        walk(root, 0);
        return { files: results.slice(0, 100), total: results.length };
      }
      case 'edit_file': {
        const fp = sanitizePath(args.path, root);
        if (!fs.existsSync(fp)) return { error: 'File not found: ' + args.path };
        let c = fs.readFileSync(fp, 'utf-8');
        if (!c.includes(args.old_string)) return { error: 'old_string not found in ' + args.path };
        c = c.replace(args.old_string, args.new_string);
        fs.writeFileSync(fp, c, 'utf-8');
        return { success: true, path: args.path };
      }
      case 'read_file_range': {
        const fp = sanitizePath(args.path, root);
        if (!fs.existsSync(fp)) return { error: 'File not found: ' + args.path };
        const c = fs.readFileSync(fp, 'utf-8');
        const lines = c.split('\n');
        const start = Math.max(0, (args.start_line || 1) - 1);
        const end = Math.min(lines.length, args.end_line || lines.length);
        return { content: lines.slice(start, end).join('\n'), path: args.path, lines: args.start_line + '-' + args.end_line, total_lines: lines.length };
      }
      case 'git_status': {
        const { execSync } = require('child_process');
        try {
          const out = execSync('git status', { cwd: root, timeout: 10000, encoding: 'utf-8' });
          return { output: out };
        } catch { return { error: 'Not a git repository or git unavailable' }; }
      }
      case 'git_diff': {
        const { execSync } = require('child_process');
        try {
          const cmd = args.path ? 'git diff -- "' + args.path + '"' : 'git diff';
          const out = execSync(cmd, { cwd: root, timeout: 10000, encoding: 'utf-8' });
          return { output: out || '(no changes)' };
        } catch { return { error: 'Not a git repository or git unavailable' }; }
      }
      default: return { error: 'Unknown tool: ' + name };

    }
  } catch (e) { return { error: e.message }; }
}

async function runAgentLoop(messages, tools, root, modelId, apiKey, maxIter) {
  const log = [];
  let msgs = [...messages];
  for (let i = 0; i < maxIter; i++) {
    const res = await fetch('https://opencode.ai/zen/go/v1/chat/completions', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', 'Authorization': 'Bearer ' + apiKey },
      body: JSON.stringify({ model: modelId, messages: msgs, tools, tool_choice: 'auto', max_tokens: 8192, temperature: 0.7 }),
      signal: AbortSignal.timeout(90000),
    });
    if (!res.ok) { const t = await res.text(); throw new Error('API error: ' + t.slice(0, 200)); }
    const data = await res.json();
    const msg = data.choices && data.choices[0] ? data.choices[0].message : null;
    if (!msg) throw new Error('Empty response from model');
    if (!msg.tool_calls) return { reply: msg.content || '', log };
    msgs.push(msg);
    for (const tc of msg.tool_calls) {
      const result = await executeAgentTool(tc, root);
      log.push({ id: tc.id, name: tc.function.name, args: JSON.parse(tc.function.arguments), result });
      msgs.push({ role: 'tool', tool_call_id: tc.id, content: JSON.stringify(result) });
    }
  }
  return { reply: 'Reached maximum iteration limit.', log };
}

