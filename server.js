/**
 * ============================================================
 *  FACELESS ANIMAL STUDIOS — STATIC DEV SERVER
 *  server.js
 *
 *  Pure Node.js — zero npm dependencies.
 *  Serves all static files from the project root.
 *
 *  On startup:
 *    1. Reads SUPABASE_URL and SUPABASE_ANON_KEY from env vars
 *    2. Writes assets/js/env.js so the browser can access them
 *    3. Starts HTTP server on process.env.PORT (default: 3000)
 *
 *  For production (Cloudflare Pages):
 *    Run `node scripts/generate-env.js` as the build command.
 *    That script does step 1–2 without starting the server.
 *    Set SUPABASE_URL, SUPABASE_ANON_KEY, and SUPABASE_SERVICE_ROLE_KEY
 *    in the CF Pages dashboard.
 *
 *  Usage:
 *    SUPABASE_URL=https://xxx.supabase.co \
 *    SUPABASE_ANON_KEY=eyJh... \
 *    SUPABASE_SERVICE_ROLE_KEY=eyJh... \
 *    node server.js
 *
 *  API endpoints (server-side, not served as static files):
 *    POST /api/dm/upload  — Authenticated DM file upload proxy.
 *                           Requires SUPABASE_SERVICE_ROLE_KEY.
 *                           See handleDMUpload() below.
 * ============================================================
 */

const http  = require('http')
