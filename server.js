//   1. Browser sends { username, ph, file_b64, file_type, file_name, file_size }
//   2. Server verifies `ph` (PBKDF2 hash) via Supabase RPC ΓÇö proves identity
//   3. Server checks member plan via service_role ΓÇö must be active/paid
//   4. Server uploads binary to Supabase Storage via service_role (bypasses RLS)
//   5. Server generates a 24-hour signed URL and returns it
//
// The SUPABASE_SERVICE_ROLE_KEY NEVER reaches the browser.
// The anon key NEVER touches Storage.
// No anon or authenticated storage policies are required on the bucket.
// The bucket should remain private with no policies (service_role only).

const UPLOAD_BUCKET     = 'dm-attachments'
const UPLOAD_MAX_BYTES  = 15 * 1024 * 1024   // 15MB body limit (10MB file + base64 overhead)
const UPLOAD_MAX_FILE_B = 10 * 1024 * 1024
const UPLOAD_ALLOWED    = new Set([
  'image/jpeg', 'image/png', 'image/gif', 'image/webp', 'image/avif',
  'audio/mpeg', 'audio/ogg', 'audio/wav', 'audio/flac', 'audio/mp4',
  'audio/x-m4a', 'audio/aiff',
  'video/mp4', 'video/webm',
  'application/pdf', 'text/plain',
])

async function handleDMUpload(req, res) {
  const SERVICE_KEY  = process.env.SUPABASE_SERVICE_ROLE_KEY
  const SUPA_URL     = process.env.SUPABASE_URL

  if (!SERVICE_KEY || !SUPA_URL) {
    console.warn('[FAS:upload] SUPABASE_SERVICE_ROLE_KEY is not set ΓÇö /api/dm/upload unavailable.')
    return sendJSON(res, 503, { error: 'File uploads are not configured on this server. Set SUPABASE_SERVICE_ROLE_KEY.' })
  }

  // Parse hostname for https.request (strip https://)
  let supabaseHost
  try {
    supabaseHost = new URL(SUPA_URL).hostname
  } catch {
    return sendJSON(res, 503, { error: 'Invalid SUPABASE_URL configuration.' })
  }

  // ΓöÇΓöÇ 1. Read body ΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇ
  let rawBody
  try {
    rawBody = await readBody(req, UPLOAD_MAX_BYTES)
  } catch (e) {
    return sendJSON(res, 413, { error: 'Request too large. Max file size is 10MB.' })
  }

  let body
  try {
    body = JSON.parse(rawBody.toString('utf8'))
  } catch {
    return sendJSON(res, 400, { error: 'Invalid JSON body.' })
  }

  const { username, ph, file_b64, file_type, file_name, file_size } = body

  // ΓöÇΓöÇ 2. Validate fields ΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇ
  if (!username || !ph || !file_b64 || !file_type || !file_name) {
    return sendJSON(res, 400, { error: 'Missing required fields.' })
  }
  if (typeof file_size === 'number' && file_size > UPLOAD_MAX_FILE_B) {
    return sendJSON(res, 400, { error: 'File too large. Maximum is 10MB.' })
  }
  if (!UPLOAD_ALLOWED.has(file_type)) {
    return sendJSON(res, 400, { error: 'File type not allowed.' })
  }
  const u = username.toLowerCase().replace(/[^a-z0-9_]/g, '')
  if (!u || u.length < 3 || u.length > 30) {
    return sendJSON(res, 400, { error: 'Invalid username.' })
  }

  // ΓöÇΓöÇ 3. Verify identity via Supabase RPC ΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇ
  // verify_member_password(p_username, p_hash) ΓåÆ bool
  // Uses service_role to bypass RLS ΓÇö no anon policy needed.
  const verifyResult = await sbRequest(supabaseHost, SERVICE_KEY, 'POST',
    '/rest/v1/rpc/verify_member_password',
    JSON.stringify({ p_username: u, p_hash: ph })
  )

  if (verifyResult.status !== 200) {
    console.error('[FAS:upload] RPC error:', verifyResult.status, verifyResult.body.toString())
    return sendJSON(res, 500, { error: 'Could not verify identity. Try again.' })
  }

  let verified
  try { verified = JSON.parse(verifyResult.body.toString()) } catch { verified = false }
  if (!verified) {
    return sendJSON(res, 401, { error: 'Authentication failed. Please sign in again.' })
  }

  // ΓöÇΓöÇ 4. Check member plan ΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇ
  const planResult = await sbRequest(supabaseHost, SERVICE_KEY, 'GET',
    `/rest/v1/member_accounts?username=eq.${encodeURIComponent(u)}&select=plan_type,member_status`,
    null
  )

  let member = null
  try {
    const rows = JSON.parse(planResult.body.toString())
    member = Array.isArray(rows) ? rows[0] : null
  } catch {}

  const activePlan = member && !['free', 'none'].includes(member.plan_type || '') && member.member_status !== 'inactive'
  if (!activePlan) {
    return sendJSON(res, 403, { error: 'File attachments require an active paid plan. Upgrade at facelessanimalstudios.com/pricing.' })
  }

  // ΓöÇΓöÇ 5. Decode base64 ΓåÆ binary Buffer ΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇ
  let fileBuffer
  try {
    fileBuffer = Buffer.from(file_b64, 'base64')
  } catch {
    return sendJSON(res, 400, { error: 'Could not decode file data.' })
  }
  if (fileBuffer.length > UPLOAD_MAX_FILE_B) {
    return sendJSON(res, 400, { error: 'Decoded file exceeds 10MB limit.' })
  }

  // ΓöÇΓöÇ 6. Upload to Supabase Storage via service_role ΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇ
  const safeName    = (file_name || 'file').replace(/[^a-zA-Z0-9._-]/g, '_').slice(0, 60)
  const storagePath = `${u}/${Date.now()}_${safeName}`

  const uploadResult = await sbStorageUpload(supabaseHost, SERVICE_KEY, UPLOAD_BUCKET, storagePath, file_type, fileBuffer)
  if (uploadResult.status !== 200 && uploadResult.status !== 201) {
    const errBody = uploadResult.body.toString()
    console.error(`[FAS:upload] Storage PUT failed (${uploadResult.status}):`, errBody)
    return sendJSON(res, 500, { error: 'File upload failed. Try again.' })
  }

  // ΓöÇΓöÇ 7. Generate signed URL (24h) ΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇ
  const signResult = await sbRequest(supabaseHost, SERVICE_KEY, 'POST',
    `/storage/v1/object/sign/${UPLOAD_BUCKET}/${storagePath}`,
    JSON.stringify({ expiresIn: 86400 })
  )

  let signedUrl = null
  try {
    const signData = JSON.parse(signResult.body.toString())
    const raw = signData.signedURL || signData.signedUrl || ''
    signedUrl = raw.startsWith('http') ? raw : `${SUPA_URL}/storage/v1${raw}`
  } catch {}

  if (!signedUrl) {
    return sendJSON(res, 500, { error: 'Upload succeeded but could not generate download link.' })
  }

  console.log(`[FAS:upload] ${u} uploaded ${safeName} (${fileBuffer.length} bytes)`)
  return sendJSON(res, 200, { url: signedUrl, path: storagePath, error: null })
}

// ΓöÇΓöÇ CONTACT FORM HANDLER ΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇ
async function handleContactForm(req, res) {
  const bodyBuf = await readBody(req, 64 * 1024) // 64KB max
  let body = {}
  const ct = (req.headers['content-type'] || '').split(';')[0].trim()

  if (ct === 'application/json') {
    try { body = JSON.parse(bodyBuf.toString('utf8')) } catch (e) {
      return sendJSON(res, 400, { error: 'Invalid JSON.' })
    }
  } else if (ct === 'application/x-www-form-urlencoded') {
    const params = new URLSearchParams(bodyBuf.toString('utf8'))
    for (const [k, v] of params) body[k] = v
  } else {
    return sendJSON(res, 415, { error: 'Unsupported content type.' })
  }

  const name    = (body.name    || '').trim().slice(0, 200)
  const email   = (body.email   || '').trim().slice(0, 200)
  const type    = (body.type    || body.request_type || body.subject || body.package || '').trim().slice(0, 200)
  const message = (body.message || body.details || body.desc || body.bio || '').trim().slice(0, 5000)
  const social  = (body.social  || body.social_handle || body.instagram || '').trim().slice(0, 200)
  const page    = (body.page    || body.source || 'unknown').trim()

  if (!name || !email) {
    return sendJSON(res, 400, { error: 'Name and email are required.' })
  }
  if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
    return sendJSON(res, 400, { error: 'Invalid email address.' })
  }

  // Store in Supabase submissions table
  if (!SUPABASE_URL) return sendJSON(res, 503, { error: 'Supabase not configured on server.' })
  const sbHost = SUPABASE_URL.replace('https://', '').replace(/\/$/, '')
  const sbKey  = SUPABASE_SERVICE_ROLE_KEY || process.env.SUPABASE_ANON_KEY

  // Capture any extra form fields not already mapped
  const { name: _n, email: _e, type: _t, request_type: _rt, subject: _s, package: _pkg,
          message: _m, details: _d, desc: _dd, bio: _b,
          social: _so, social_handle: _sh, instagram: _ig, page: _pg, source: _src, ...rest } = body
  const extra  = JSON.stringify({ type, social, page, ...rest })

  const payload = JSON.stringify({
    submission_type: 'contact_inquiry',
    display_name:    name,
    username:        email,
    bio:             message,
    extra_notes:     extra,
    status:          'submitted',
    links_json:      [],
    selected_plan:   'none',
    payment_verified: false,
  })

  let saved = false
  try {
    const r = await sbRequest(sbHost, sbKey, 'POST', '/rest/v1/submissions', payload)
    saved = r.status >= 200 && r.status < 300
    if (!saved) console.error(`[FAS:contact] DB save failed (status ${r.status}) for ${email}`)
  } catch (e) {
    console.error(`[FAS:contact] DB error: ${e.message}`)
  }

  if (!saved) {
    console.log(`[FAS:contact] Fallback log ΓÇö From: ${name} <${email}> ΓÇö ${type} ΓÇö ${message.slice(0, 100)}`)
  } else {
    console.log(`[FAS:contact] Inquiry from ${email} (${name}) stored.`)
  }

  return sendJSON(res, 200, { ok: true, message: 'Inquiry received.' })
}


