// ── STEP 3: HTTP SERVER ──────────────────────────────────────────────
const server = http.createServer(handleRequest)

function handleRequest(req, res) {
  // Strip query strings and decode URI
  let urlPath
  try {
    urlPath = decodeURIComponent(req.url.split('?')[0])
  } catch {
    send(res, 400, 'text/plain', 'Bad Request')
    return
  }

  // ── API ROUTES (server-side, not static files) ─────────────
  // ── RADIO API ROUTES ───────────────────────────────────────────────
  if (urlPath.startsWith('/api/radio/')) {
    res.setHeader('Access-Control-Allow-Origin', '*')
    res.setHeader('Access-Control-Allow-Methods', 'GET, POST, DELETE, OPTIONS')
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type, Authorization, X-FAS-Username')
    if (req.method === 'OPTIONS') { send(res, 204, 'text/plain', ''); return }

    if (urlPath === '/api/radio/tracks') {
      if (req.method !== 'GET') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }
      handleRadioTracks(req, res)
      return
    }
    if (urlPath === '/api/radio/upload') {
      if (req.method !== 'POST') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }
      handleRadioUpload(req, res).catch(err => {
        console.error('[FAS:radio:upload] Unhandled error:', err.message)
        if (!res.headersSent) sendJSON(res, 500, { error: 'Internal server error.' })
      })
      return
    }
    if (urlPath === '/api/radio/delete') {
      if (req.method !== 'POST') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }
      handleRadioDelete(req, res).catch(err => {
        console.error('[FAS:radio:delete] Unhandled error:', err.message)
        if (!res.headersSent) sendJSON(res, 500, { error: 'Internal server error.' })
      })
      return
    }
    if (urlPath === '/api/radio/community-upload') {
      if (req.method !== 'POST') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }
      handleCommunityUpload(req, res).catch(err => {
        console.error('[FAS:community:upload] Unhandled error:', err.message)
        if (!res.headersSent) sendJSON(res, 500, { error: 'Internal server error.' })
      })
      return
    }


    sendJSON(res, 404, { error: 'Not found.' })
    return
  }

  if (urlPath.startsWith('/api/dm/')) {
    res.setHeader('Access-Control-Allow-Origin', '*')
    res.setHeader('Access-Control-Allow-Methods', 'GET, POST, OPTIONS')
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type')
    if (req.method === 'OPTIONS') { send(res, 204, 'text/plain', ''); return }

    if (urlPath === '/api/dm/upload') {
      if (req.method !== 'POST') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }
      handleDMUpload(req, res).catch(err => {
        console.error('[FAS:upload] Unhandled error:', err.message)
        if (!res.headersSent) sendJSON(res, 500, { error: 'Internal server error.' })
      })
      return
    }

    if (urlPath === '/api/dm/threads') {
      if (req.method !== 'GET') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }
      handleDMThreads(req, res).catch(err => {
        console.error('[FAS:dm:threads] Unhandled error:', err.message)
        if (!res.headersSent) sendJSON(res, 500, { error: 'Internal server error.' })
      })
      return
    }

    if (urlPath === '/api/dm/messages') {
      if (req.method !== 'GET') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }
      handleDMMessages(req, res).catch(err => {
        console.error('[FAS:dm:messages] Unhandled error:', err.message)
        if (!res.headersSent) sendJSON(res, 500, { error: 'Internal server error.' })
      })
      return
    }

    if (urlPath === '/api/dm/send') {
      if (req.method !== 'POST') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }
      handleDMSend(req, res).catch(err => {
        console.error('[FAS:dm:send] Unhandled error:', err.message)
        if (!res.headersSent) sendJSON(res, 500, { error: 'Internal server error.' })
      })
      return
    }

    if (urlPath === '/api/dm/mark-read') {
      if (req.method !== 'POST') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }
      handleDMMarkRead(req, res).catch(err => {
        console.error('[FAS:dm:mark-read] Unhandled error:', err.message)
        if (!res.headersSent) sendJSON(res, 500, { error: 'Internal server error.' })
      })
      return
    }

    sendJSON(res, 404, { error: 'Not found.' })
    return
  }

  // ── MUSIC UPLOAD API ─────────────────────────────────────────────────
  if (urlPath.startsWith('/api/music/')) {
    res.setHeader('Access-Control-Allow-Origin', '*')
    res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS')
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type')
    if (req.method === 'OPTIONS') { send(res, 204, 'text/plain', ''); return }

    if (urlPath === '/api/music/upload') {
      if (req.method !== 'POST') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }
      handleMusicUpload(req, res).catch(err => {
        console.error('[FAS:music:upload] Unhandled error:', err.message)
        if (!res.headersSent) sendJSON(res, 500, { error: 'Internal server error.' })
      })
      return
    }
    if (urlPath === '/api/music/delete') {
      if (req.method !== 'POST') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }
      handleMusicDelete(req, res).catch(err => {
        console.error('[FAS:music:delete] Unhandled error:', err.message)
        if (!res.headersSent) sendJSON(res, 500, { error: 'Internal server error.' })
      })
      return
    }

    sendJSON(res, 404, { error: 'Not found.' })
    return
  }

  // ── CONTACT FORM API ──────────────────────────────────────────────
  if (urlPath === '/api/admin/users/set-role') {
    res.setHeader('Access-Control-Allow-Origin', '*')
    res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS')
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type')
    if (req.method === 'OPTIONS') { send(res, 204, 'text/plain', ''); return }
    if (req.method !== 'POST') { sendJSON(res, 405, { ok: false, error: 'Method not allowed.', code: 'METHOD_NOT_ALLOWED' }); return }

    handleAdminSetRole(req, res).catch(err => {
      console.error('[FAS:admin:set-role] Unhandled error:', err.message)
      if (!res.headersSent) sendJSON(res, 500, { ok: false, error: 'Internal server error.', code: 'INTERNAL_ERROR' })
    })
    return
  }

  // ── CONTACT FORM API ──────────────────────────────────────────────
  if (urlPath === '/api/contact') {
    res.setHeader('Access-Control-Allow-Origin', '*')
    res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS')
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type')
    if (req.method === 'OPTIONS') { send(res, 204, 'text/plain', ''); return }
    if (req.method !== 'POST') { sendJSON(res, 405, { error: 'Method not allowed.' }); return }

    handleContactForm(req, res).catch(err => {
      console.error('[FAS:contact] Unhandled error:', err.message)
      if (!res.headersSent) sendJSON(res, 500, { error: 'Internal server error.' })
    })
    return
  }

  // Default to index.html
  if (urlPath === '/' || urlPath === '') {
    urlPath = '/index.html'
  }

  const parts = urlPath.replace(/^\//, '').split('/')

  // ── ADMIN ROUTES ─────────────────────────────────────────────
  // Admin files live under /admin/*.
  //
  // All /admin requests require a valid ADMIN_TOKEN env var and a
  // matching X-Admin-Token header. Without a valid token the request
  // is always blocked with 403 — there is no fallthrough to
  // client-side auth at the HTTP layer.
  //
  // To access the admin area:
  //   1. Set ADMIN_TOKEN in Replit Secrets (long random string).
  //   2. Add X-Admin-Token: <value> to browser requests (e.g. via
  //      a browser extension like ModHeader, or a Cloudflare
  //      Transform Rule / Access policy in production).
  //
  // Client-side auth (Supabase session + ADMIN_EMAILS allowlist in
  // admin-auth.js) provides a second independent layer of protection
  // once the page is loaded.
  if (urlPath.startsWith('/admin')) {
    console.log(`[FAS:admin] ${req.method} ${urlPath}`)
    const adminToken = process.env.ADMIN_TOKEN
    const provided   = req.headers['x-admin-token']

    if (!adminToken || !provided || provided !== adminToken) {
      send(res, 403, 'text/html; charset=utf-8',
        '<html><body style="font-family:system-ui;background:#0a0a0a;color:#f0f0f0;display:flex;align-items:center;justify-content:center;min-height:100vh;margin:0;">' +
        '<div style="text-align:center;"><h1 style="color:#f87171;margin-bottom:0.5rem;">403 — Forbidden</h1>' +
        '<p style="color:#555;">You do not have permission to access this area.</p>' +
        '<p style="margin-top:1rem;"><a href="/" style="color:#c9a96e;">← Back to site</a></p>' +
        '</div></body></html>'
      )
      return
    }
  }

  // ── Dynamic creator/business page routing ─────────────────────
  // /artist/[slug]   → templates/artist.html
  // /creator/[slug]  → templates/creator.html
  // /business/[slug] → templates/business.html
  const routeType = parts[0]
  if (['artist', 'creator', 'business'].includes(routeType) && parts[1] && parts[1] !== '') {
    return serveFile(res, path.join(ROOT, 'templates', `${routeType}.html`))
  }

  const filePath = path.join(ROOT, urlPath)

  // Block directory traversal
  if (!filePath.startsWith(ROOT + path.sep) && filePath !== ROOT) {
    send(res, 403, 'text/plain', 'Forbidden')
    return
  }

  serveFile(res, filePath)
}

// ── ADMIN ROLE ACTIONS (trusted server path) ─────────────────────────
// Supports only:
//   - make_moderator  -> role: moderator
//   - remove_moderator -> role: user
async function handleAdminSetRole(req, res) {
  let rawBody
  try { rawBody = await readBody(req, 8192) } catch {
    return sendJSON(res, 400, { ok: false, error: 'Invalid request body.', code: 'INVALID_BODY' })
  }

  let body
  try { body = JSON.parse(rawBody.toString('utf8')) } catch {
    return sendJSON(res, 400, { ok: false, error: 'Invalid JSON body.', code: 'INVALID_JSON' })
  }

  const actorUsername = String(body && body.actor_username || '').toLowerCase().trim()
  const ph            = String(body && body.ph || '').trim()
  const targetUserId  = String(body && body.target_user_id || '').trim()
  const action        = String(body && body.action || '').toLowerCase().trim()

  if (!actorUsername || !ph || !targetUserId || !action) {
    return sendJSON(res, 400, { ok: false, error: 'Missing required fields.', code: 'MISSING_FIELDS' })
  }

  if (action !== 'make_moderator' && action !== 'remove_moderator') {
    return sendJSON(res, 400, { ok: false, error: 'Invalid action.', code: 'INVALID_ACTION' })
  }

  const actor = actorUsername.replace(/[^a-z0-9_-]/g, '')
  if (!actor) {
    return sendJSON(res, 400, { ok: false, error: 'Invalid actor username.', code: 'INVALID_ACTOR' })
  }

  let creds
  try { creds = getServiceCreds() } catch {
    return sendJSON(res, 503, { ok: false, error: 'Server not available.', code: 'SERVER_NOT_CONFIGURED' })
  }

  const ok = await verifyIdentity(creds.host, creds.key, actor, ph)
  if (!ok) {
    return sendJSON(res, 401, { ok: false, error: 'Authentication failed.', code: 'UNAUTHENTICATED' })
  }

  let actorRow = null
  try {
    const actorRes = await sbRequest(
      creds.host,
      creds.key,
      'GET',
      `/rest/v1/member_accounts?username=eq.${encodeURIComponent(actor)}&select=id,username,role&limit=1`,
      null
    )
    const rows = JSON.parse(actorRes.body.toString())
    actorRow = Array.isArray(rows) ? rows[0] : null
  } catch {}

  if (!actorRow) {
    return sendJSON(res, 403, { ok: false, error: 'Actor account not found.', code: 'ACTOR_NOT_FOUND' })
  }
  if (String(actorRow.role || 'user').toLowerCase() !== 'super_admin') {
    return sendJSON(res, 403, { ok: false, error: 'Only super_admin can change roles.', code: 'FORBIDDEN_NOT_SUPER_ADMIN' })
  }

  let targetRow = null
  try {
    const targetRes = await sbRequest(
      creds.host,
      creds.key,
      'GET',
      `/rest/v1/member_accounts?id=eq.${encodeURIComponent(targetUserId)}&select=id,username,role&limit=1`,
      null
    )
    const rows = JSON.parse(targetRes.body.toString())
    targetRow = Array.isArray(rows) ? rows[0] : null
  } catch {}

  if (!targetRow) {
    return sendJSON(res, 404, { ok: false, error: 'Target user not found.', code: 'TARGET_NOT_FOUND' })
  }

  if (String(targetRow.id || '') === String(actorRow.id || '')) {
    return sendJSON(res, 403, { ok: false, error: 'You cannot modify your own role.', code: 'FORBIDDEN_SELF_TARGET' })
  }

  const previousRole = String(targetRow.role || 'user').toLowerCase()
  if (previousRole === 'super_admin') {
    return sendJSON(res, 403, { ok: false, error: 'You cannot modify a super_admin account.', code: 'FORBIDDEN_TARGET_SUPER_ADMIN' })
  }

  const nextRole = action === 'make_moderator' ? 'moderator' : 'user'

  try {
    const updateRes = await sbRequest(
      creds.host,
      creds.key,
      'PATCH',
      `/rest/v1/member_accounts?id=eq.${encodeURIComponent(targetUserId)}`,
      JSON.stringify({ role: nextRole })
    )

    if (updateRes.status < 200 || updateRes.status >= 300) {
      return sendJSON(res, 500, { ok: false, error: 'Could not update role.', code: 'UPDATE_FAILED' })
    }
  } catch {
    return sendJSON(res, 500, { ok: false, error: 'Could not update role.', code: 'UPDATE_FAILED' })
  }

  sendJSON(res, 200, {
    ok: true,
    message: nextRole === 'moderator' ? 'User updated to moderator.' : 'Moderator role removed.',
    action,
    target: {
      id: targetRow.id,
      username: targetRow.username,
      previous_role: previousRole,
      next_role: nextRole,
    },
  })
}

function serveFile(res, filePath) {
  fs.readFile(filePath, (err, data) => {
    if (!err) {
      const ext  = path.extname(filePath).toLowerCase()
      const mime = MIME_TYPES[ext] || 'application/octet-stream'
      res.writeHead(200, {
        'Content-Type':  mime,
        'Cache-Control': 'no-cache',
      })
      res.end(data)
      return
    }

    if (err.code === 'ENOENT') {
      // Try appending .html (e.g. /about → /about.html)
      if (!path.extname(filePath)) {
        return serveFile(res, filePath + '.html')
      }
      send(res, 404, 'text/html; charset=utf-8',
        '<h1 style="font-family:system-ui">404 — Not Found</h1>')
    } else if (err.code === 'EISDIR') {
      // Directory requested — serve index.html inside it
      return serveFile(res, path.join(filePath, 'index.html'))
    } else {
      send(res, 500, 'text/plain', 'Internal Server Error')
    }
  })
}

function send(res, status, type, body) {
  res.writeHead(status, { 'Content-Type': type })
  res.end(body)
}

// ── RADIO TRACK HANDLERS ─────────────────────────────────────────────

const AUDIO_DIR   = path.join(ROOT, 'assets', 'audio')
const RADIO_ADMINS = new Set(['jamespropane00', 'arianamnm'])   // usernames with radio upload/delete access
const RADIO_MAX_BYTES = 60 * 1024 * 1024  // 60MB body limit (50MB file + base64 overhead)
const RADIO_MAX_FILE  = 50 * 1024 * 1024  // 50MB per track
const RADIO_ALLOWED   = new Set([
  'audio/mpeg', 'audio/ogg', 'audio/wav', 'audio/flac', 'audio/mp4',
  'audio/x-m4a', 'audio/aiff', 'audio/aac',
])

// Channel config: ch1 keeps existing paths for backward compat; ch4/ch5 get their own dirs
const CHANNELS = {
  '1': { dir: AUDIO_DIR,                                  json: path.join(AUDIO_DIR, 'tracks.json')   },
  '4': { dir: path.join(AUDIO_DIR, 'ch4'),                json: path.join(AUDIO_DIR, 'tracks-4.json') },
  '5': { dir: path.join(AUDIO_DIR, 'ch5'),                json: path.join(AUDIO_DIR, 'tracks-5.json') },
}

function getChannel(ch) {
  return CHANNELS[ch] || CHANNELS['1']
}

function readChannelTracks(ch) {
  const { json } = getChannel(ch)
  try { return JSON.parse(fs.readFileSync(json, 'utf8')) } catch { return [] }
}

function writeChannelTracks(tracks, ch) {
  const { json, dir } = getChannel(ch)
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true })
  fs.writeFileSync(json, JSON.stringify(tracks, null, 2), 'utf8')
}

/** GET /api/radio/tracks?channel=1|4|5 — public, returns track list for a channel */
function handleRadioTracks(req, res) {
  const u   = new URL(req.url, 'http://localhost')
  const ch  = CHANNELS[u.searchParams.get('channel')] ? u.searchParams.get('channel') : '1'
  sendJSON(res, 200, { tracks: readChannelTracks(ch), channel: ch })
}

/** POST /api/radio/upload — admin only, saves audio file + updates channel tracks */
async function handleRadioUpload(req, res) {
  let rawBody
  try {
    rawBody = await readBody(req, RADIO_MAX_BYTES)
  } catch {
    return sendJSON(res, 413, { error: 'File too large. Max 50MB.' })
  }

  let body
  try { body = JSON.parse(rawBody.toString('utf8')) } catch {
    return sendJSON(res, 400, { error: 'Invalid JSON body.' })
  }

  const { username, ph, file_b64, file_type, file_name, title, channel } = body
  const ch = CHANNELS[channel] ? String(channel) : '1'

  if (!username || !ph || !file_b64 || !file_type || !file_name || !title) {
    return sendJSON(res, 400, { error: 'Missing required fields.' })
  }

  const u = username.toLowerCase().replace(/[^a-z0-9_]/g, '')
  if (!RADIO_ADMINS.has(u)) {
    return sendJSON(res, 403, { error: 'Admin access only.' })
  }

  if (!RADIO_ALLOWED.has(file_type)) {
    return sendJSON(res, 400, { error: 'Only audio files are allowed (mp3, wav, ogg, flac, m4a, aac).' })
  }

  let creds
  try { creds = getServiceCreds() } catch {
    return sendJSON(res, 503, { error: 'Server not configured with service credentials.' })
  }

  const ok = await verifyIdentity(creds.host, creds.key, u, ph)
  if (!ok) return sendJSON(res, 401, { error: 'Authentication failed.' })

  let fileBuffer
  try { fileBuffer = Buffer.from(file_b64, 'base64') } catch {
    return sendJSON(res, 400, { error: 'Could not decode file data.' })
  }
  if (fileBuffer.length > RADIO_MAX_FILE) {
    return sendJSON(res, 400, { error: 'File exceeds 50MB limit.' })
  }

  const { dir } = getChannel(ch)
  const ext      = path.extname(file_name).toLowerCase() || '.mp3'
  const safeName = (path.basename(file_name, ext) || 'track').replace(/[^a-zA-Z0-9._-]/g, '_').slice(0, 60)
  const fileName = Date.now() + '_' + safeName + ext
  const filePath = path.join(dir, fileName)
  const srcPath  = ch === '1' ? 'assets/audio/' + fileName : 'assets/audio/ch' + ch + '/' + fileName

  try {
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true })
    fs.writeFileSync(filePath, fileBuffer)
  } catch (e) {
    console.error('[FAS:radio:upload] File write error:', e.message)
    return sendJSON(res, 500, { error: 'Could not save file.' })
  }

  const tracks = readChannelTracks(ch)
  const entry  = { id: fileName, title: title.trim().slice(0, 100), src: srcPath, uploadedAt: new Date().toISOString() }
  tracks.push(entry)
  try { writeChannelTracks(tracks, ch) } catch (e) {
    console.error('[FAS:radio:upload] tracks write error:', e.message)
    return sendJSON(res, 500, { error: 'File saved but track list could not be updated.' })
  }

  console.log('[FAS:radio:upload] Channel', ch, 'uploaded:', fileName, '(' + fileBuffer.length + ' bytes)')
  sendJSON(res, 200, { ok: true, track: entry, channel: ch })
}

