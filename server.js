function serveFile(res, filePath) {
  fs.readFile(filePath, (err, data) => {
    if (!err) {
      const ext  = path.extname(filePath).toLowerCase()
      const mime = MIME_TYPES[ext] || 'application/octet-stream'
      res.writeHead(200, {
        'Content-Type':  mime,
        'Cache-Control': 'no-cache',
      })
      res.end(data)
      return
    }

    if (err.code === 'ENOENT') {
      // Try appending .html (e.g. /about ΓåÆ /about.html)
      if (!path.extname(filePath)) {
        return serveFile(res, filePath + '.html')
      }
      send(res, 404, 'text/html; charset=utf-8',
        '<h1 style="font-family:system-ui">404 ΓÇö Not Found</h1>')
    } else if (err.code === 'EISDIR') {
      // Directory requested ΓÇö serve index.html inside it
      return serveFile(res, path.join(filePath, 'index.html'))
    } else {
      send(res, 500, 'text/plain', 'Internal Server Error')
    }
  })
}

