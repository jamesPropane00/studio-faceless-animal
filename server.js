// This file is loaded by the browser before any Supabase-aware modules.
// It sets window.__FAS_CONFIG so supabase-client.js can read credentials
// without hardcoding them into source code.
writeEnvConfig()

function writeEnvConfig() {
  const url  = process.env.SUPABASE_URL      || ''
  const key  = process.env.SUPABASE_ANON_KEY || ''
  const ready = Boolean(url && key)

  const content = [
    '/* AUTO-GENERATED by server.js ΓÇö do not edit manually.',
    '   Set SUPABASE_URL and SUPABASE_ANON_KEY env vars and restart the server.',
    '   This file is gitignored. The server regenerates it on every startup. */',
    '',
    'window.__FAS_CONFIG = {',
    `  supabaseUrl:     ${JSON.stringify(url)},`,
    `  supabaseAnonKey: ${JSON.stringify(key)},`,
    `  ready:           ${JSON.stringify(ready)},`,
    '}',
  ].join('\n')

  try {
    fs.writeFileSync(path.join(ROOT, 'assets', 'js', 'env.js'), content, 'utf8')
  } catch (e) {
    console.warn('[FAS] Could not write assets/js/env.js:', e.message)
  }

  if (ready) {
    console.log('[FAS] Supabase configured ΓÇö client will be active in the browser.')
  } else {
    console.log('[FAS] Supabase not configured ΓÇö running in static preview mode.')
    console.log('[FAS]   To activate: set SUPABASE_URL and SUPABASE_ANON_KEY, then restart.')
  }
}


