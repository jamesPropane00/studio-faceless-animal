/**
 * GET /api/dm/threads?username=X
 * Authorization: Bearer <ph>
 * Returns all conversation threads for the authenticated user.
 */
async function handleDMThreads(req, res) {
  let creds
  try { creds = getServiceCreds() } catch (e) {
    return sendJSON(res, 503, { error: 'DM service unavailable.' })
  }

  const qs = new URL(req.url, `http://localhost`).searchParams
  const username = (qs.get('username') || '').toLowerCase().replace(/[^a-z0-9_-]/g, '')
  // ph MUST come from the Authorization header (never the URL)
  const authHeader = req.headers['authorization'] || ''
  const ph = authHeader.startsWith('Bearer ') ? authHeader.slice(7).trim() : ''

  if (!username || !ph) return sendJSON(res, 400, { error: 'Missing username or auth token.' })

  const ok = await verifyIdentity(creds.host, creds.key, username, ph)
  if (!ok) return sendJSON(res, 401, { error: 'Authentication failed.' })

  const me = await getMemberByUsername(creds.host, creds.key, username)
  if (!me || me.dms_enabled === false) {
    return sendJSON(res, 403, { error: 'Direct messages are disabled for this account.' })
  }

  const filter = `or=(sender.eq.${encodeURIComponent(username)},recipient.eq.${encodeURIComponent(username)})`
    + `&select=sender,recipient,message,file_name,created_at,read_at`
    + `&order=created_at.desc`
  const result = await sbRequest(creds.host, creds.key, 'GET', `/rest/v1/dm_messages?${filter}`, null)
  if (result.status !== 200) return sendJSON(res, 500, { error: 'Could not load threads.' })

  let rows
  try { rows = JSON.parse(result.body.toString()) } catch { rows = [] }

  // Deduplicate into thread summaries
  const seen = {}
  ;(rows || []).forEach(row => {
    const partner = row.sender === username ? row.recipient : row.sender
    const preview = row.file_name ? `≡ƒôÄ ${row.file_name}` : row.message
    if (!seen[partner]) {
      seen[partner] = { username: partner, last_message: preview, last_ts: row.created_at,
        unread: row.recipient === username && !row.read_at ? 1 : 0 }
    } else if (row.recipient === username && !row.read_at) {
      seen[partner].unread++
    }
  })
  const threads = Object.values(seen).sort((a, b) => new Date(b.last_ts) - new Date(a.last_ts))
  sendJSON(res, 200, { threads })
}

